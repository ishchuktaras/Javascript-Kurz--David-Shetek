/* **************
 * Do while cyklus
 */

// For - známe počet opakování
for (var i = 0; i < 0; i++){
	console.log('Taras Ishchuk');
}

// While - neznáme počet opakování, nemusí se provést ani jednou
var i = 0;
while (i < 0){
	console.log('Taras Ishchuk');
	i++;
}

// Do while - neznáme počet opakování, provede se minimálně jednou
var i = 0;
	do {
		console.log('Taras Ishchuk');
		i++;
	} while (i < 0); 
	