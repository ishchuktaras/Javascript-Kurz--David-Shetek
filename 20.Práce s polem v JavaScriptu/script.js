/************
*    Array (Pole) 
*/

var employees = ['David' ,'Jane', 'Diana'];

/*
	push - přidá na konec pole,
	unshift - přidá na začátek,

	pop - odebere poslední,
	shift - odebere první
*/

employees.push('John');
employees.unshift('Albert');

employees.pop();
employees.shift();

console.log(employees);