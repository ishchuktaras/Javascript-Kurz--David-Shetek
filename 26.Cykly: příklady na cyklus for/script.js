/* **************
 * Procházení pole cyklem for
 */

// Vypíšte 12x své jméno a příjmeni
for (var i = 0; i < 12; i++){
	console.log('Taras Ishchuk');
}

// Vypíšte čísla od 0 do 10
for (var i = 0; i <= 10; i++){
	console.log(i);
}

// Vypíšte čísla od 5 do 50 včetně, ale jen násobky pětí
for (var i = 5; i <= 50; i = i + 5){
	console.log(i);
}

// Vypíšte čísla od 15 do 0 (tedy v obráceném pořadí)
for (var i = 15; i >= 0; i--){
	console.log(i);
}